import DuoUserSettingsService from "../../service/mfa/duoUserSettingsService";

class GetStartedWithDuoController {
  /**
   * GetStartedWithDuoController constructor
   * @param {Worker} worker
   * @param {string} requestId uuid
   * @param {ApiClientOptions} apiClientOptions
   */
  constructor(worker, requestId, apiClientOptions) {
    this.worker = worker;
    this.requestId = requestId;
    this.duoUserSettingsService = new DuoUserSettingsService(apiClientOptions);
  }

  /**
   * Controller executor.
   * @returns {Promise<void>}
   */
  async _exec() {
    try {
      const result = await this.exec.apply(this, arguments);
      this.worker.port.emit(this.requestId, "SUCCESS", result);
    } catch (error) {
      console.error(error);
      this.worker.port.emit(this.requestId, "ERROR", error);
    }
  }

  /**
   * Retrieve the OTP setup info
   * @returns {Promise<void>}
   */
  async exec() {
    await this.duoUserSettingsService.startSetup();
  }
}

export default GetStartedWithDuoController;
