import ExtensionPermissionsService from "../../service/extension/extensionPermissionsService";

export default class IsExtensionAllowedOnEveryWebsiteController {
  /**
   * Constructor
   * @param {Worker} worker
   * @param {string} requestId uuid
   */
  constructor(worker, requestId) {
    this.worker = worker;
    this.requestId = requestId;
  }

  /**
   * Wrapper of exec function to run it with worker.
   * @return {Promise<void>}
   */
  async _exec() {
    try {
      const result = await this.exec.apply(this, arguments);
      this.worker.port.emit(this.requestId, "SUCCESS", result);
    } catch (error) {
      console.error(error);
      this.worker.port.emit(this.requestId, "ERROR", error);
    }
  }

  /**
   * Check if the extension is allowed on every website.
   * @return {Promise<boolean>}
   */
  async exec() {
    return ExtensionPermissionsService.isAllowedOnEveryWebsite();
  }
}
